    /// <summary>
    /// Story 4-5 T2.3 / D1 — strategies whose emission goes through
    /// <see cref="ProjectionRoleBodyEmitter.EmitStandardDataGrid"/> with
    /// <c>emitExpandableRows: true</c>. Default + ActionQueue dispatch directly; Dashboard
    /// delegates to <see cref="ProjectionRoleBodyEmitter.EmitDefaultBody"/>. StatusOverview
    /// is included so its no-enum fallback path (which routes through <c>EmitDefaultBody</c>)
    /// has the required <c>_ephemeralViewKey</c> field and <c>ExpandedRowState</c> property
    /// in scope; the normal StatusOverview emission (synthetic <c>StatusOverviewRow</c>) does
    /// not reference the fields, so the unconditional injection is harmless. Timeline and
    /// DetailRecord render different shells (no DataGrid) and are excluded per D1.
    /// </summary>
    private static bool EmitsExpandInRowMachinery(ProjectionRenderStrategy strategy)
        => strategy == ProjectionRenderStrategy.Default
            || strategy == ProjectionRenderStrategy.ActionQueue
            || strategy == ProjectionRenderStrategy.StatusOverview
            || strategy == ProjectionRenderStrategy.Dashboard;

    private static bool HasBadgeMappings(RazorModel model) {
        foreach (ColumnModel col in model.Columns) {
            if (col.BadgeMappings.Count > 0) {
                return true;
            }
        }

        return false;
    }

    private static bool NeedsShellLocalizer(RazorModel model)
        // Story 4-6 review fix (D3): revert to conditional emission so non-using views do not
        // ship a superfluous DI injection. Empty-state secondary text now resolves inside
        // FcProjectionEmptyPlaceholder itself (D17 follow-up), so views without badges,
        // descriptions, or unsupported columns no longer pull in IStringLocalizer.
        => HasBadgeMappings(model)
            || EmitsExpandInRowMachinery(model.Strategy)
            || HasUnsupportedColumns(model)
            || HasColumnDescriptions(model);

    private static bool HasUnsupportedColumns(RazorModel model) {
        foreach (ColumnModel col in model.Columns) {
            if (col.TypeCategory == TypeCategory.Unsupported) {
                return true;
            }
        }

        return false;
    }

    private static bool HasColumnDescriptions(RazorModel model) {
        foreach (ColumnModel col in model.Columns) {
            if (!string.IsNullOrWhiteSpace(col.Description)) {
                return true;
            }
        }

        return false;
    }

    private static string ResolveDomIdSuffix(string value) {
        StringBuilder sb = new(value.Length);
        foreach (char c in value) {
            if ((c >= 'a' && c <= 'z')
                || (c >= 'A' && c <= 'Z')
                || (c >= '0' && c <= '9')) {
                _ = sb.Append(c);
            }
            else {
                _ = sb.Append('-');
            }
        }

        return sb.ToString();
    }

    private static void EmitStrategyMembers(StringBuilder sb, RazorModel model) {
        // Story 4-4 T2.1 / T2.5 — grid-rendering strategies emit the stable per-view key,
        // the compile-time-resolved item-key accessor, and a DotNetObjectReference for the
        // JS-side scroll throttle's invokeMethodAsync callback.
        if (RoleBodyHelpers.IsGridRenderingStrategy(model.Strategy)) {
            string viewKey = RoleBodyHelpers.ResolveViewKey(model);
            string itemKeyExpr = RoleBodyHelpers.ResolveItemKeyAccessorExpression(model);
            _ = sb.AppendLine("    private const string _viewKey = \"" + RoleBodyHelpers.EscapeString(viewKey) + "\";");
