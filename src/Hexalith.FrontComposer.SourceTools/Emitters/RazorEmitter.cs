    }

    private static void EmitStatusOverviewSortHelper(StringBuilder sb, RazorModel model) {
        ColumnModel? statusColumn = RoleBodyHelpers.ResolveStatusEnumColumn(model);

        _ = sb.AppendLine("    private static long GetEnumSortOrder(Enum? value)");
        _ = sb.AppendLine("    {");
        _ = sb.AppendLine("        if (value is null)");
        _ = sb.AppendLine("        {");
        _ = sb.AppendLine("            return long.MaxValue;");
        _ = sb.AppendLine("        }");
        _ = sb.AppendLine();

        if (statusColumn is not null && statusColumn.EnumMemberNames.Count > 0) {
            _ = sb.AppendLine("        return value.ToString() switch");
            _ = sb.AppendLine("        {");
            for (int i = 0; i < statusColumn.EnumMemberNames.Count; i++) {
                string memberName = RoleBodyHelpers.EscapeString(statusColumn.EnumMemberNames[i]);
                _ = sb.AppendLine("            \"" + memberName + "\" => " + i.ToString(CultureInfo.InvariantCulture) + "L,");
            }

            _ = sb.AppendLine("            _ => long.MaxValue - 1,");
            _ = sb.AppendLine("        };");
        }
        else {
            _ = sb.AppendLine("        return long.MaxValue - 1;");
        }

        _ = sb.AppendLine("    }");
        _ = sb.AppendLine();
    }

    private static void EmitLifecycleHooks(StringBuilder sb, RazorModel model) {
        bool isGrid = RoleBodyHelpers.IsGridRenderingStrategy(model.Strategy);

        _ = sb.AppendLine("    /// <inheritdoc />");
        _ = sb.AppendLine("    protected override void OnInitialized()");
        _ = sb.AppendLine("    {");
        _ = sb.AppendLine("        " + model.TypeName + "State.StateChanged += OnStateChanged;");
        if (isGrid) {
            _ = sb.AppendLine("        LoadedPageState.StateChanged += OnStateChanged;");
            _ = sb.AppendLine("        GridNavigationState.StateChanged += OnStateChanged;");
            _ = sb.AppendLine("        ReconciliationSweepState.StateChanged += OnStateChanged;");
        }
        if (EmitsExpandInRowMachinery(model.Strategy)) {
            _ = sb.AppendLine("        ExpandedRowState.StateChanged += OnStateChanged;");
        }
        _ = sb.AppendLine("    }");
        _ = sb.AppendLine();

        _ = sb.AppendLine("    private void OnStateChanged(object? sender, EventArgs e)");
        _ = sb.AppendLine("        => InvokeAsync(StateHasChanged);");
        _ = sb.AppendLine();

        if (isGrid) {
            // Story 4-4 T2.6 — unified OnAfterRenderAsync with ordered hook list. First-render
            // only: capture DotNetObjectReference for the JS scroll throttle. Within-session
            // scroll restore is gated on the hydrated GridViewSnapshot (Story 3-6 emits the
            // RestoreGridStateAction; this hook simply replays the hydrated ScrollTop via JS).
            _ = sb.AppendLine("    /// <inheritdoc />");
            _ = sb.AppendLine("    protected override async Task OnAfterRenderAsync(bool firstRender)");
            _ = sb.AppendLine("    {");
            _ = sb.AppendLine("        if (firstRender)");
            _ = sb.AppendLine("        {");
            _ = sb.AppendLine("            // Story 4-4 T2.5 / D4 — DotNetObjectReference scoped to the view; disposed in DisposeAsync.");
            _ = sb.AppendLine("            _dotNetRef = global::Microsoft.JSInterop.DotNetObjectReference.Create<object>(this);");
            _ = sb.AppendLine();
            _ = sb.AppendLine("            // Story 4-4 T2.6 / AC7 — within-session scroll restore. Cross-session is clamped to 0 by D5.");
            _ = sb.AppendLine("            try");
            _ = sb.AppendLine("            {");
            _ = sb.AppendLine("                var snapshot = CurrentGridSnapshot();");
            _ = sb.AppendLine("                if (snapshot is not null && snapshot.ScrollTop > 0)");
            _ = sb.AppendLine("                {");
            _ = sb.AppendLine("                    await ScrollInterop.ScrollToOffsetAsync(_viewKey, snapshot.ScrollTop).ConfigureAwait(true);");
            _ = sb.AppendLine("                }");
            _ = sb.AppendLine("            }");
            _ = sb.AppendLine("            catch (global::Microsoft.JSInterop.JSDisconnectedException) { /* circuit teardown */ }");
            _ = sb.AppendLine("            catch (System.Threading.Tasks.TaskCanceledException) { /* circuit teardown */ }");
            _ = sb.AppendLine("        }");
            _ = sb.AppendLine();
            _ = sb.AppendLine("        RegisterVisibleProjectionLane();");
            _ = sb.AppendLine("    }");
            _ = sb.AppendLine();

