    private static void EmitNumericConverters(StringBuilder sb, CommandFormModel form) {
        bool emittedHumanize = false;
        foreach (FormFieldModel field in form.Fields) {
            if (field.TypeCategory != FormFieldTypeCategory.Select) {
                continue;
            }

            if (!emittedHumanize) {
                EmitHumanizeHelper(sb);
                emittedHumanize = true;
            }
        }

        foreach (FormFieldModel field in form.Fields) {
            if (field.TypeCategory != FormFieldTypeCategory.NumberInput
                && field.TypeCategory != FormFieldTypeCategory.DecimalInput) {
                continue;
            }

            bool isInteger = field.TypeCategory == FormFieldTypeCategory.NumberInput;
            string numericType = field.TypeName switch {
                "Int32" => "int",
                "Int64" => "long",
                "Decimal" => "decimal",
                "Double" => "double",
                "Single" => "float",
                _ => "int",
            };

            // Restricted NumberStyles (patch P14): integer path forbids exponents/currency;
            // float path allows decimal separators + thousands but still disallows currency / parentheses.
            string numberStyles = isInteger
                ? "NumberStyles.Integer | NumberStyles.AllowLeadingSign | NumberStyles.AllowTrailingWhite | NumberStyles.AllowLeadingWhite"
                : "NumberStyles.Float | NumberStyles.AllowThousands";

            bool propertyIsNullable = field.IsNullable;

            _ = sb.AppendLine();
            _ = sb.AppendLine("    private void On" + field.PropertyName + "Changed(string? value)");
            _ = sb.AppendLine("    {");
            _ = sb.AppendLine("        _" + field.PropertyName + "String = value;");
            _ = sb.AppendLine("        if (string.IsNullOrWhiteSpace(value))");
            _ = sb.AppendLine("        {");
            if (propertyIsNullable) {
                _ = sb.AppendLine("            _model." + field.PropertyName + " = null;");
                _ = sb.AppendLine("            _" + field.PropertyName + "ParseError = null;");
            }
            else {
                // Empty is ambiguous for a non-nullable value type; leave the previous value untouched
                // and flag the input as required (patch P4).
                _ = sb.AppendLine("            _" + field.PropertyName + "ParseError = \"A number is required.\";");
            }

            _ = sb.AppendLine("            return;");
            _ = sb.AppendLine("        }");
            _ = sb.AppendLine("        if (" + numericType + ".TryParse(value, " + numberStyles + ", CultureInfo.CurrentCulture, out var parsed))");
            _ = sb.AppendLine("        {");
            // Reject non-finite for floating-point types so NaN / Infinity cannot round-trip.
            if (numericType is "double" or "float") {
                _ = sb.AppendLine("            if (" + numericType + ".IsFinite(parsed))");
                _ = sb.AppendLine("            {");
                _ = sb.AppendLine("                _model." + field.PropertyName + " = parsed;");
                _ = sb.AppendLine("                _" + field.PropertyName + "ParseError = null;");
                _ = sb.AppendLine("            }");
                _ = sb.AppendLine("            else");
                _ = sb.AppendLine("            {");
                _ = sb.AppendLine("                _" + field.PropertyName + "ParseError = \"NaN and Infinity are not allowed.\";");
                _ = sb.AppendLine("            }");
            }
            else {
                _ = sb.AppendLine("            _model." + field.PropertyName + " = parsed;");
                _ = sb.AppendLine("            _" + field.PropertyName + "ParseError = null;");
            }

            _ = sb.AppendLine("        }");
            _ = sb.AppendLine("        else");
            _ = sb.AppendLine("        {");
            _ = sb.AppendLine("            _" + field.PropertyName + "ParseError = \"Invalid number format.\";");
            _ = sb.AppendLine("        }");
            _ = sb.AppendLine("    }");
        }
    }

    private static void EmitHumanizeHelper(StringBuilder sb) {
        int maxLen = CommandFormTransform.EnumOptionLabelMaxLength;
        _ = sb.AppendLine();
        // Story 2-2 code-review P38 — char.IsUpper covers non-ASCII uppercase (Ü, Ñ, É).
        // Story 2-2 code-review P37 — truncate on rune boundary to avoid splitting UTF-16 surrogate pairs.
        _ = sb.AppendLine("    private static string HumanizeEnumLabel(string value)");
        _ = sb.AppendLine("    {");
        _ = sb.AppendLine("        if (string.IsNullOrEmpty(value)) return value;");
        _ = sb.AppendLine("        var sb = new System.Text.StringBuilder(value.Length + 8);");
        _ = sb.AppendLine("        for (int i = 0; i < value.Length; i++)");
        _ = sb.AppendLine("        {");
        _ = sb.AppendLine("            char c = value[i];");
        _ = sb.AppendLine("            if (i == 0) { sb.Append(char.ToUpperInvariant(c)); continue; }");
        _ = sb.AppendLine("            bool isUpper = char.IsUpper(c);");
        _ = sb.AppendLine("            bool prevIsLower = char.IsLower(value[i - 1]);");
        _ = sb.AppendLine("            if (isUpper && prevIsLower) { sb.Append(' '); sb.Append(c); }");
        _ = sb.AppendLine("            else { sb.Append(c); }");
        _ = sb.AppendLine("        }");
        _ = sb.AppendLine("        string label = sb.ToString();");
        _ = sb.AppendLine("        if (label.Length <= " + maxLen + ") return label;");
        _ = sb.AppendLine("        int cutoff = " + (maxLen - 1) + ";");
        _ = sb.AppendLine("        if (cutoff > 0 && char.IsHighSurrogate(label[cutoff - 1])) { cutoff--; }");
        _ = sb.AppendLine("        return label.Substring(0, cutoff) + \"\\u2026\";");
        _ = sb.AppendLine("    }");
    }

    /// <summary>
    /// Escapes a string for embedding in a C# double-quoted literal. Delegates to Roslyn's
