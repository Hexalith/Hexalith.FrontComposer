using System.Collections.Immutable;
using System.Text;

using Hexalith.FrontComposer.SourceTools.Parsing;
using Hexalith.FrontComposer.SourceTools.Transforms;

namespace Hexalith.FrontComposer.SourceTools.Emitters;

/// <summary>
/// Emits a compilation-level MCP manifest from SourceTools IR.
/// </summary>
public static class McpManifestEmitter {
    public const string GeneratedHintName = "FrontComposerMcpManifest.g.cs";

    public static string Emit(ImmutableArray<CommandModel> commands, ImmutableArray<DomainModel> projections) {
        Dictionary<string, int> commandBaseCounts = CountCommandBaseNames(commands);
        Dictionary<string, int> resourceBaseCounts = CountProjectionBaseUris(projections);
        List<McpCommandDescriptorModel> commandDescriptors = [];
        List<McpResourceDescriptorModel> resourceDescriptors = [];

        foreach (CommandModel command in commands) {
            string boundedContext = string.IsNullOrWhiteSpace(command.BoundedContext) ? "Default" : command.BoundedContext!;
            string baseName = McpManifestTransform.BuildCommandProtocolName(boundedContext, command.Namespace, command.TypeName, false);
            bool disambiguate = commandBaseCounts.TryGetValue(baseName, out int commandCount) && commandCount > 1;
            commandDescriptors.Add(McpManifestTransform.TransformCommand(command, disambiguate));
        }

        foreach (DomainModel projection in projections) {
            string boundedContext = string.IsNullOrWhiteSpace(projection.BoundedContext) ? "Default" : projection.BoundedContext!;
            string baseUri = McpManifestTransform.BuildProjectionUri(boundedContext, projection.Namespace, projection.TypeName, false);
            bool disambiguate = resourceBaseCounts.TryGetValue(baseUri, out int resourceCount) && resourceCount > 1;
            resourceDescriptors.Add(McpManifestTransform.TransformProjection(projection, disambiguate));
        }

        commandDescriptors.Sort((left, right) => string.CompareOrdinal(left.ProtocolName, right.ProtocolName));
        resourceDescriptors.Sort((left, right) => string.CompareOrdinal(left.ProtocolUri, right.ProtocolUri));
        GeneratedSchemaFingerprint[] commandFingerprints = [.. commandDescriptors
            .Select(d => SchemaFingerprintTransform.CreateCommandPayload(d).Fingerprint)];
        GeneratedSchemaFingerprint[] resourceFingerprints = [.. resourceDescriptors
            .Select(d => SchemaFingerprintTransform.CreateResourcePayload(d).Fingerprint)];
        GeneratedSchemaFingerprint lifecycleFingerprint = SchemaFingerprintTransform.CreateLifecycleResultPayload().Fingerprint;
        GeneratedSchemaFingerprint[] rendererFingerprints = [.. resourceDescriptors
            .Select(d => SchemaFingerprintTransform.CreateMarkdownRendererPayload(
                d.ProtocolUri + "#markdown-renderer",
                d.RenderStrategy,
                maxCharacters: 64_000,
                maxFieldCharacters: 4_096).Fingerprint)];
        GeneratedSchemaFingerprint aggregateFingerprint = SchemaFingerprintTransform.CreateAggregateManifestPayload(
            commandFingerprints,
            resourceFingerprints,
            lifecycleFingerprint,
            rendererFingerprints,
            []).Fingerprint;

        var sb = new StringBuilder(4096);
        _ = sb.AppendLine("// Generated by FrontComposer source generator");
        _ = sb.AppendLine("// Do not edit directly");
        _ = sb.AppendLine();
        _ = sb.AppendLine("#nullable enable");
        _ = sb.AppendLine();
        _ = sb.AppendLine("using System;");
        _ = sb.AppendLine("using System.Collections.Generic;");
        _ = sb.AppendLine("using Hexalith.FrontComposer.Contracts.Mcp;");
        _ = sb.AppendLine("using Hexalith.FrontComposer.Contracts.Schema;");
        _ = sb.AppendLine();
        _ = sb.AppendLine("namespace Hexalith.FrontComposer.Generated;");
        _ = sb.AppendLine();
        _ = sb.AppendLine("[GeneratedManifest]");
        _ = sb.AppendLine("internal static class FrontComposerMcpGeneratedManifest");
        _ = sb.AppendLine("{");
        _ = sb.AppendLine("    public static McpManifest Manifest { get; } = new McpManifest(");
        _ = sb.AppendLine("        \"" + Escape(McpManifestTransform.SchemaVersion) + "\",");
        EmitCommands(sb, commandDescriptors);
        _ = sb.AppendLine(",");
        EmitResources(sb, resourceDescriptors);
        _ = sb.AppendLine(",");
        _ = sb.Append("        ");
        EmitFingerprint(sb, aggregateFingerprint);
        _ = sb.AppendLine(");");
        _ = sb.AppendLine("}");
        return sb.ToString();
    }

    private static Dictionary<string, int> CountCommandBaseNames(ImmutableArray<CommandModel> commands) {
        Dictionary<string, int> counts = new(StringComparer.OrdinalIgnoreCase);
        foreach (CommandModel command in commands) {
            string boundedContext = string.IsNullOrWhiteSpace(command.BoundedContext) ? "Default" : command.BoundedContext!;
            string key = McpManifestTransform.BuildCommandProtocolName(boundedContext, command.Namespace, command.TypeName, false);
            counts[key] = counts.TryGetValue(key, out int count) ? count + 1 : 1;
        }

        return counts;
    }

    private static Dictionary<string, int> CountProjectionBaseUris(ImmutableArray<DomainModel> projections) {
        Dictionary<string, int> counts = new(StringComparer.OrdinalIgnoreCase);
        foreach (DomainModel projection in projections) {
            string boundedContext = string.IsNullOrWhiteSpace(projection.BoundedContext) ? "Default" : projection.BoundedContext!;
            string key = McpManifestTransform.BuildProjectionUri(boundedContext, projection.Namespace, projection.TypeName, false);
            counts[key] = counts.TryGetValue(key, out int count) ? count + 1 : 1;
        }

        return counts;
    }

    private static void EmitCommands(StringBuilder sb, IReadOnlyList<McpCommandDescriptorModel> commands) {
        _ = sb.AppendLine("        new List<McpCommandDescriptor>");
        _ = sb.AppendLine("        {");
        foreach (McpCommandDescriptorModel command in commands) {
            _ = sb.AppendLine("            new McpCommandDescriptor(");
            _ = sb.AppendLine("                \"" + Escape(command.ProtocolName) + "\",");
            _ = sb.AppendLine("                \"" + Escape(command.CommandTypeName) + "\",");
            _ = sb.AppendLine("                \"" + Escape(command.BoundedContext) + "\",");
            _ = sb.AppendLine("                \"" + Escape(command.Title) + "\",");
            _ = sb.AppendLine("                " + Literal(command.Description) + ",");
            _ = sb.AppendLine("                " + Literal(command.AuthorizationPolicyName) + ",");
            EmitParameters(sb, command.Parameters, 16);
            _ = sb.AppendLine(",");
            _ = sb.Append("                ");
            EmitStrings(sb, command.DerivablePropertyNames, 16);
            _ = sb.AppendLine(",");
            _ = sb.Append("                ");
            EmitFingerprint(sb, SchemaFingerprintTransform.CreateCommandPayload(command).Fingerprint);
            _ = sb.AppendLine("),");
        }

        _ = sb.Append("        }");
    }

    private static void EmitResources(StringBuilder sb, IReadOnlyList<McpResourceDescriptorModel> resources) {
        _ = sb.AppendLine("        new List<McpResourceDescriptor>");
        _ = sb.AppendLine("        {");
        foreach (McpResourceDescriptorModel resource in resources) {
            _ = sb.AppendLine("            new McpResourceDescriptor(");
            _ = sb.AppendLine("                \"" + Escape(resource.ProtocolUri) + "\",");
            _ = sb.AppendLine("                \"" + Escape(resource.Name) + "\",");
            _ = sb.AppendLine("                \"" + Escape(resource.ProjectionTypeName) + "\",");
            _ = sb.AppendLine("                \"" + Escape(resource.BoundedContext) + "\",");
            _ = sb.AppendLine("                \"" + Escape(resource.Title) + "\",");
            _ = sb.AppendLine("                " + Literal(resource.Description) + ",");
            EmitParameters(sb, resource.Fields, 16);
            _ = sb.AppendLine(",");
            // P-18: validate the strategy identifier matches a defined McpProjectionRenderStrategy
            // member before interpolating it into generated source. Anything else falls back to
            // Default rather than producing a non-compiling (or worse, attacker-influenced)
            // generated file. Today the upstream transform already passes enum.ToString(), but
            // the constructor accepts any string, so the constraint is enforced at the emit edge.
            string strategy = SanitizeRenderStrategy(resource.RenderStrategy);
            _ = sb.AppendLine("                McpProjectionRenderStrategy." + strategy + ",");
            _ = sb.AppendLine("                " + Literal(resource.EntityPluralLabel) + ",");
            _ = sb.AppendLine("                " + Literal(resource.EmptyStateCtaCommandName) + ",");
            _ = sb.Append("                ");
            EmitFingerprint(sb, SchemaFingerprintTransform.CreateResourcePayload(resource).Fingerprint);
            _ = sb.AppendLine("),");
        }

        _ = sb.Append("        }");
    }

    private static void EmitParameters(StringBuilder sb, IReadOnlyList<McpParameterDescriptorModel> parameters, int indent) {
        string pad = new(' ', indent);
        _ = sb.AppendLine(pad + "new List<McpParameterDescriptor>");
        _ = sb.AppendLine(pad + "{");
        foreach (McpParameterDescriptorModel parameter in parameters) {
            _ = sb.AppendLine(pad + "    new McpParameterDescriptor(");
            _ = sb.AppendLine(pad + "        \"" + Escape(parameter.Name) + "\",");
            _ = sb.AppendLine(pad + "        \"" + Escape(parameter.TypeName) + "\",");
            _ = sb.AppendLine(pad + "        \"" + Escape(parameter.JsonType) + "\",");
            _ = sb.AppendLine(pad + "        " + Bool(parameter.IsRequired) + ",");
            _ = sb.AppendLine(pad + "        " + Bool(parameter.IsNullable) + ",");
            _ = sb.AppendLine(pad + "        \"" + Escape(parameter.Title) + "\",");
            _ = sb.AppendLine(pad + "        " + Literal(parameter.Description) + ",");
            _ = sb.Append(pad + "        ");
            EmitStrings(sb, parameter.EnumValues, indent + 8);
            _ = sb.AppendLine(",");
            _ = sb.AppendLine(pad + "        " + Bool(parameter.IsUnsupported) + ",");
            _ = sb.Append(pad + "        ");
            EmitStringDictionary(sb, parameter.BadgeMappings, indent + 8);
            _ = sb.AppendLine(",");
            _ = sb.AppendLine(pad + "        \"" + Escape(parameter.DisplayFormat) + "\"),");
        }

        _ = sb.Append(pad + "}");
    }

    private static void EmitStrings(StringBuilder sb, IReadOnlyList<string> values, int indent) {
        string pad = new(' ', indent);
        _ = sb.Append("new List<string>");
        if (values.Count == 0) {
            _ = sb.Append("()");
            return;
        }

        _ = sb.AppendLine();
        _ = sb.AppendLine(pad + "{");
        foreach (string value in values) {
            _ = sb.AppendLine(pad + "    \"" + Escape(value) + "\",");
        }

        _ = sb.Append(pad + "}");
    }

    private static void EmitStringDictionary(StringBuilder sb, IReadOnlyDictionary<string, string> values, int indent) {
        string pad = new(' ', indent);
        _ = sb.Append("new Dictionary<string, string>(StringComparer.Ordinal)");
        if (values.Count == 0) {
            return;
        }

        _ = sb.AppendLine();
        _ = sb.AppendLine(pad + "{");
        foreach (KeyValuePair<string, string> pair in values.OrderBy(p => p.Key, StringComparer.Ordinal)) {
            _ = sb.AppendLine(pad + "    [\"" + Escape(pair.Key) + "\"] = \"" + Escape(pair.Value) + "\",");
        }

        _ = sb.Append(pad + "}");
    }

    private static void EmitFingerprint(StringBuilder sb, GeneratedSchemaFingerprint fingerprint)
        => sb.Append("new SchemaFingerprint(\"")
            .Append(Escape(fingerprint.AlgorithmId))
            .Append("\", \"")
            .Append(Escape(fingerprint.Value))
            .Append("\", \"")
            .Append(Escape(fingerprint.CanonicalizerVersion))
            .Append("\", \"")
            .Append(Escape(fingerprint.TestVectorId))
            .Append("\")");

    private static string Literal(string? value)
        => value is null ? "null" : "\"" + Escape(value) + "\"";

    private static string Bool(bool value) => value ? "true" : "false";

    private static readonly HashSet<string> KnownRenderStrategies = new(StringComparer.Ordinal) {
        "Default",
        "ActionQueue",
        "StatusOverview",
        "DetailRecord",
        "Timeline",
        "Dashboard",
    };

    /// <summary>
    /// P-16: a render-strategy value that does not match a defined <c>McpProjectionRenderStrategy</c>
    /// member is a generator-time defect — most likely a new enum member that was added in
    /// Contracts without updating <see cref="KnownRenderStrategies"/>. Throwing surfaces the
    /// drift instead of silently producing <c>Default</c> and fingerprinting the wrong value.
    /// </summary>
    private static string SanitizeRenderStrategy(string value) {
        if (string.IsNullOrEmpty(value)) {
            return "Default";
        }

        if (!KnownRenderStrategies.Contains(value)) {
            throw new InvalidOperationException(
                "Unknown McpProjectionRenderStrategy value '" + value + "' encountered during manifest emission. "
                + "Add the new strategy to McpManifestEmitter.KnownRenderStrategies (and update the schema fingerprint test vector).");
        }

        return value;
    }

    private static string Escape(string value) {
        var sb = new StringBuilder(value.Length);
        foreach (char c in value) {
            switch (c) {
                case '\\':
                    _ = sb.Append("\\\\");
                    break;
                case '"':
                    _ = sb.Append("\\\"");
                    break;
                case '\r':
                    _ = sb.Append("\\r");
                    break;
                case '\n':
                    _ = sb.Append("\\n");
                    break;
                case '\t':
                    _ = sb.Append("\\t");
                    break;
                case '\0':
                    _ = sb.Append("\\0");
                    break;
                default:
                    // Escape all other control characters and the JS line separators U+2028/U+2029
                    // so the generated source compiles deterministically and downstream JSON encoders
                    // do not face ambiguous payloads.
                    if (char.IsControl(c) || c == '\u2028' || c == '\u2029') {
                        _ = sb.Append("\\u").Append(((int)c).ToString("x4", System.Globalization.CultureInfo.InvariantCulture));
                    }
                    else {
                        _ = sb.Append(c);
                    }

                    break;
            }
        }

        return sb.ToString();
    }
}
