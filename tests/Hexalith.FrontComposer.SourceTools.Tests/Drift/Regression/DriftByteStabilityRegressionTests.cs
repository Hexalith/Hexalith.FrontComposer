    [Fact]
    public void DriftDiagnostics_AgainstSamePartials_AreInvariantToPartialOrder() {
        // CM-16 — partial-declaration order also must not change drift comparison.
        const string partA = """
            using Hexalith.FrontComposer.Contracts.Attributes;
            namespace TestDomain;
            [BoundedContext("Orders")]
            [Projection]
            public partial class OrderProjection {
                public string Id { get; set; } = string.Empty;
            }
            """;
        const string partB = """
            namespace TestDomain;
            public partial class OrderProjection {
                public string AddedField { get; set; } = string.Empty;
            }
            """;

        IReadOnlyList<Diagnostic> forward = RunAndCollectDriftDiagnostics([partA, partB], ValidBaseline);
        IReadOnlyList<Diagnostic> reverse = RunAndCollectDriftDiagnostics([partB, partA], ValidBaseline);

        forward.Select(d => d.Id + "|" + d.GetMessage(System.Globalization.CultureInfo.InvariantCulture))
            .OrderBy(s => s, StringComparer.Ordinal)
            .ShouldBe(reverse.Select(d => d.Id + "|" + d.GetMessage(System.Globalization.CultureInfo.InvariantCulture))
                .OrderBy(s => s, StringComparer.Ordinal),
                "AC18 — drift diagnostics must be invariant to partial-declaration order.");
    }

