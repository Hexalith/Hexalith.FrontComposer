    }

    private static IReadOnlyList<Diagnostic> RunAndCollectAllDiagnostics(string[] sources, string baselineJson, bool driftEnabled) {
        CancellationToken ct = TestContext.Current.CancellationToken;
        CSharpCompilation compilation = CompilationHelper.CreateCompilation(sources);
        FrontComposerGenerator generator = new();
        AdditionalText baseline = new InMemoryAdditionalText("frontcomposer.drift-baseline.json", baselineJson);

        AnalyzerConfigOptionsProvider options = driftEnabled
            ? CompilationHelper.DriftEnabledOptions()
            : EmptyOptionsProvider.Instance;
        GeneratorDriver driver = CSharpGeneratorDriver.Create(
            generators: [generator.AsSourceGenerator()],
            additionalTexts: [baseline],
            optionsProvider: options);
        driver = driver.RunGenerators(compilation, ct);
        return driver.GetRunResult().Diagnostics;
    }

    private static bool IsDriftDiagnostic(string id)
        => id.Length == 7
        && id.StartsWith("HFC", StringComparison.Ordinal)
        && int.TryParse(id.AsSpan("HFC".Length), System.Globalization.NumberStyles.None, System.Globalization.CultureInfo.InvariantCulture, out int n)
        && n is >= 1058 and <= 1099;

    private static void AssertSameSetByHint((string Hint, string Source)[] left, (string Hint, string Source)[] right, string message) {
        Dictionary<string, string> leftMap = left.ToDictionary(t => t.Hint, t => t.Source, StringComparer.Ordinal);
        Dictionary<string, string> rightMap = right.ToDictionary(t => t.Hint, t => t.Source, StringComparer.Ordinal);
        leftMap.Keys.OrderBy(k => k, StringComparer.Ordinal)
            .ShouldBe(rightMap.Keys.OrderBy(k => k, StringComparer.Ordinal), message);
        foreach (string hint in leftMap.Keys) {
            leftMap[hint].ShouldBe(rightMap[hint], $"{message} (hint={hint})");
        }
    }

    private sealed class EmptyOptionsProvider : AnalyzerConfigOptionsProvider {
        internal static EmptyOptionsProvider Instance { get; } = new();

        public override AnalyzerConfigOptions GlobalOptions { get; } = new EmptyOptions();
        public override AnalyzerConfigOptions GetOptions(SyntaxTree tree) => GlobalOptions;
        public override AnalyzerConfigOptions GetOptions(AdditionalText textFile) => GlobalOptions;

        private sealed class EmptyOptions : AnalyzerConfigOptions {
            public override bool TryGetValue(string key, out string value) {
                value = string.Empty;
                return false;
            }
        }
    }
