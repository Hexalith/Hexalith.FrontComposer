    private static (string Hint, string Source)[] RunAndCollectGenerated(string[] sources, string? baselineJson, bool driftEnabled = true) {
        CancellationToken ct = TestContext.Current.CancellationToken;
        CSharpCompilation compilation = CompilationHelper.CreateCompilation(sources);
        FrontComposerGenerator generator = new();
        AdditionalText[] texts = baselineJson is null
            ? []
            : [new InMemoryAdditionalText("frontcomposer.drift-baseline.json", baselineJson)];

        AnalyzerConfigOptionsProvider options = driftEnabled
            ? CompilationHelper.DriftEnabledOptions()
            : EmptyOptionsProvider.Instance;
        GeneratorDriver driver = CSharpGeneratorDriver.Create(
            generators: [generator.AsSourceGenerator()],
            additionalTexts: texts,
            optionsProvider: options);
        driver = driver.RunGenerators(compilation, ct);

        GeneratorRunResult result = driver.GetRunResult().Results[0];
        return [.. result.GeneratedSources.Select(s => (s.HintName, s.SourceText.ToString()))];
    }

    private static (string Hint, string Source)[] RunAndCollectGeneratedMultiBaseline(string[] sources, (string Path, string Content)[] baselines) {
        CancellationToken ct = TestContext.Current.CancellationToken;
        CSharpCompilation compilation = CompilationHelper.CreateCompilation(sources);
        FrontComposerGenerator generator = new();
        AdditionalText[] texts = [.. baselines.Select(b => (AdditionalText)new InMemoryAdditionalText(b.Path, b.Content))];

        GeneratorDriver driver = CSharpGeneratorDriver.Create(
            generators: [generator.AsSourceGenerator()],
            additionalTexts: texts,
            optionsProvider: CompilationHelper.DriftEnabledOptions());
        driver = driver.RunGenerators(compilation, ct);

        GeneratorRunResult result = driver.GetRunResult().Results[0];
        return [.. result.GeneratedSources.Select(s => (s.HintName, s.SourceText.ToString()))];
    }

    private static IReadOnlyList<Diagnostic> RunAndCollectDriftDiagnostics(string[] sources, string baselineJson) {
        CancellationToken ct = TestContext.Current.CancellationToken;
        CSharpCompilation compilation = CompilationHelper.CreateCompilation(sources);
        FrontComposerGenerator generator = new();
        AdditionalText baseline = new InMemoryAdditionalText("frontcomposer.drift-baseline.json", baselineJson);

        GeneratorDriver driver = CSharpGeneratorDriver.Create(
            generators: [generator.AsSourceGenerator()],
            additionalTexts: [baseline],
            optionsProvider: CompilationHelper.DriftEnabledOptions());
        driver = driver.RunGenerators(compilation, ct);

        return [.. driver.GetRunResult().Diagnostics.Where(d => IsDriftDiagnostic(d.Id))];
    }

    private static IReadOnlyList<Diagnostic> RunAndCollectDriftDiagnosticsMultiBaseline(string[] sources, (string Path, string Content)[] baselines) {
        CancellationToken ct = TestContext.Current.CancellationToken;
