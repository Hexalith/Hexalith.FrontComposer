    [Fact]
    public void GeneratedOutput_IsByteIdenticalAcrossRepeatedRuns_WithBaselinePresent() {
        // CC-1 — no sort: compare in emit order including hint names so an ordering churn shows.
        (string Hint, string Source)[] runOne = RunAndCollectGenerated([ProjectionA, ProjectionB], ValidBaseline);
        (string Hint, string Source)[] runTwo = RunAndCollectGenerated([ProjectionA, ProjectionB], ValidBaseline);

        runOne.ShouldBe(runTwo, "AC18 — generated output (hint+source, in emit order) must be byte-identical across repeated runs.");
    }

    [Fact]
    public void GeneratedOutput_IsByteIdenticalAcrossInputFileOrderPermutations() {
        (string Hint, string Source)[] forward = RunAndCollectGenerated([ProjectionA, ProjectionB], ValidBaseline);
        (string Hint, string Source)[] reverse = RunAndCollectGenerated([ProjectionB, ProjectionA], ValidBaseline);

        // Order-equivalence: file order must not change the generated set, but emit order may
        // depend on input order. Compare as sets keyed by hint name.
        Dictionary<string, string> forwardMap = forward.ToDictionary(t => t.Hint, t => t.Source, StringComparer.Ordinal);
        Dictionary<string, string> reverseMap = reverse.ToDictionary(t => t.Hint, t => t.Source, StringComparer.Ordinal);
        forwardMap.Keys.OrderBy(k => k, StringComparer.Ordinal)
            .ShouldBe(reverseMap.Keys.OrderBy(k => k, StringComparer.Ordinal),
                "AC18 — input file order must not change the set of emitted hint names.");
        foreach (string hint in forwardMap.Keys) {
            forwardMap[hint].ShouldBe(reverseMap[hint],
                $"AC18 — input file order must not change generated bytes (hint={hint}).");
        }
    }

