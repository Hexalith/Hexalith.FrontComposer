    [Fact]
    public void DuplicateBaselineIdentity_AcrossFiles_IsRejectedDeterministicallyInBothOrders() {
        // CM-9 — AC9 deterministic-fail-closed under file-order swap.
        const string baselineDup1 = """
            { "schemaVersion": "frontcomposer.generated-ui-baseline.v1",
              "algorithm": "frontcomposer-structural-v1",
              "contracts": [{ "family": "projection", "type": "TestDomain.A", "boundedContext": "Orders",
                "properties": [{ "name": "Id", "category": "String", "nullable": false }] }] }
            """;
        const string baselineDup2 = """
            { "schemaVersion": "frontcomposer.generated-ui-baseline.v1",
              "algorithm": "frontcomposer-structural-v1",
              "contracts": [{ "family": "projection", "type": "TestDomain.A", "boundedContext": "Orders",
                "properties": [{ "name": "Notes", "category": "String", "nullable": true }] }] }
            """;

        // Baselines must use the documented `frontcomposer.drift-baseline*.json` prefix
        // (Story 9-1 P5) to be recognized as drift baselines.
        IReadOnlyList<Diagnostic> forward = RunAndCollectDriftDiagnosticsMultiBaseline(
            [ProjectionA],
            [("frontcomposer.drift-baseline-dup1.json", baselineDup1),
             ("frontcomposer.drift-baseline-dup2.json", baselineDup2)]);
        IReadOnlyList<Diagnostic> reverse = RunAndCollectDriftDiagnosticsMultiBaseline(
            [ProjectionA],
            [("frontcomposer.drift-baseline-dup2.json", baselineDup2),
             ("frontcomposer.drift-baseline-dup1.json", baselineDup1)]);

        // HFC1064 (BaselineIdentityInvalid) MUST fire in both orderings deterministically.
        forward.Any(d => d.Id == FcDiagnosticIds.HFC1064_GeneratedUiBaselineIdentityInvalid).ShouldBeTrue(
            "AC9 — duplicate baseline identity must produce HFC1064.");
        reverse.Any(d => d.Id == FcDiagnosticIds.HFC1064_GeneratedUiBaselineIdentityInvalid).ShouldBeTrue(
            "AC9 — duplicate baseline identity must produce HFC1064 regardless of file order.");
    }

    [Fact]
    public void DriftDetection_DoesNotRewriteGeneratedOutput_GateOffVsGateOn() {
        // CC-2 — compare HfcDriftDetectionEnabled=false vs =true with the same source AND
        // baseline. Both must emit byte-identical generated output: drift is diagnostics-only.
        (string Hint, string Source)[] gateOff = RunAndCollectGenerated([ProjectionA], ValidBaseline, driftEnabled: false);
        (string Hint, string Source)[] gateOn = RunAndCollectGenerated([ProjectionA], ValidBaseline, driftEnabled: true);

        AssertSameSetByHint(gateOff, gateOn,
            "AC18 — drift detection is diagnostics-only and MUST NOT alter generated output.");
    }

    [Fact]
    public void GateOff_EmitsNoDriftDiagnostics() {
        // CL-6 — when drift is gated off, no diagnostics in the drift HFC range escape.
        IReadOnlyList<Diagnostic> diagnostics = RunAndCollectAllDiagnostics([ProjectionA], ValidBaseline, driftEnabled: false);
        Diagnostic[] driftDiagnostics = [.. diagnostics.Where(d => IsDriftDiagnostic(d.Id))];
        driftDiagnostics.ShouldBeEmpty(
            "AC18 — gate-off path must produce zero drift diagnostics (current: " + string.Join(", ", driftDiagnostics.Select(d => d.Id)) + ").");
    }

    [Fact]
    public void DriftDiagnosticOrdering_IsByteStableAcrossRepeatedRuns() {
        // CH-10 — make the harness produce drift diagnostics so ordering instability is exposed.
        const string driftedSource = """
            using Hexalith.FrontComposer.Contracts.Attributes;
            namespace TestDomain;
            [BoundedContext("Orders")]
            [Projection]
            public partial class OrderProjection {
                public string Id { get; set; } = string.Empty;
                public string AddedFieldOne { get; set; } = string.Empty;
                public string AddedFieldTwo { get; set; } = string.Empty;
            }
            """;

        IReadOnlyList<Diagnostic> runOne = RunAndCollectDriftDiagnostics([driftedSource], ValidBaseline);
        IReadOnlyList<Diagnostic> runTwo = RunAndCollectDriftDiagnostics([driftedSource], ValidBaseline);

        // Compare as ordered sequences — sort masks the very ordering bug AC18 forbids.
        runOne.Count.ShouldBeGreaterThan(0, "Test setup: expected drift diagnostics from added properties.");
        runOne.Select(d => d.Id).ShouldBe(runTwo.Select(d => d.Id),
            "AC18 — diagnostic emission order must be byte-stable across repeated runs.");
    }

    [Fact]
    public void GeneratedOutput_BomVsNoBom_IsByteIdentical() {
        // CH-11 — production strips UTF-8 BOM (DriftDetection.cs ~480-496); a regression must show.
